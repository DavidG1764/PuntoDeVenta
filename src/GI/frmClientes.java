    private void comparacion(String correo,String contraseña){
        String correocomp=correo;
        String contraseñacomp=contraseña;
        boolean encontrado=false;
        try{
            objp.leer();
            for(clientes e:objp.array){
                if(correocomp.equals(e.getCorreo()) && contraseñacomp.equals(e.getContraseña())){
                    JOptionPane.showMessageDialog(rootPane,"Sesion iniciada");
                    String nombre=e.getNombre(),f;
                    encontrado=true;
                    f=nombre.substring(0,1).toUpperCase()+nombre.substring(1);
                    frmProductos abrir=new frmProductos();
                    abrir.nombremostrado(f);
                    abrir.setVisible(true);
                    this.setVisible(false);
                    break;
                }
            }
            if(!encontrado){
                JOptionPane.showMessageDialog(rootPane, "Usuario no enontrado");
            }
                
            }catch(IOException e){
              JOptionPane.showMessageDialog(rootPane,"Error al insertar los datos: "+ e.getMessage());
        }
    }
    
