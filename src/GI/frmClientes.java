    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String correo,contraseña,correo2,contraseña2,nombre;
        correo=txtCorreo.getText();
        correo2=txtCorreo2.getText();
        contraseña=txtContraseña.getText();
        contraseña2=txtContraseña2.getText();
        nombre=txtNombre.getText();
        if(!"".equals(correo) && !"".equals(correo2) && !"".equals(contraseña) && !"".equals(contraseña2)&&!"".equals(nombre)){
            if(correo.equals(correo2)&&contraseña.equals(contraseña2)){
                clientes obje=new clientes(correo,contraseña,nombre);
                try{
                    objp.insertar(obje);
                    
                }catch(IOException e){
                    JOptionPane.showMessageDialog(rootPane, ""+e.getMessage());
            }
                limpiar();
                btnRegistrar.setVisible(false);
                txtContraseña2.setVisible(false);
                txtCorreo2.setVisible(false);
                txtcCont.setVisible(false);
                txtCCor.setVisible(false);
                btnIniciar.setEnabled(true);
                lblRegistrar.setVisible(true);
               lblNombre.setVisible(false);
               txtNombre.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(rootPane, "El correo o  contraseña no son iguales");
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Nada debe estar vacio");
        }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

